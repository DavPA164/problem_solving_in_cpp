// hello_world.cpp - This program prints messages on the display terminal

#include <iostream>                     //a preprocessor directive

int main()                              //Function header
{                                       //Curly braces express grouping
    std::cout << "Hello World";         //message
    std::cout << std::endl;             //start a new line
    std::cout << "C++ is great fun!";   
    return 0;                           //return 0 to the caller
}                                       //end of main function